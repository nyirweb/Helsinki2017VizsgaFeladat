package helsinki2017.model;

public class DontosVersenyzokModel 
{
    String nev;
    String orszag;
    String kurTechnikaiPontszam;
    String kurKomponensiPontszam;
    String kurHibaPontszam;

    public DontosVersenyzokModel() {
    }

    public String getNev() {
        return nev;
    }

    public void setNev(String nev) {
        this.nev = nev;
    }

    public String getOrszag() {
        return orszag;
    }

    public void setOrszag(String orszag) {
        this.orszag = orszag;
    }

    public String getKurTechnikaiPontszam() {
        return kurTechnikaiPontszam;
    }

    public void setKurTechnikaiPontszam(String kurTechnikaiPontszam) {
        this.kurTechnikaiPontszam = kurTechnikaiPontszam;
    }

    public String getKurKomponensiPontszam() {
        return kurKomponensiPontszam;
    }

    public void setKurKomponensiPontszam(String kurKomponensiPontszam) {
        this.kurKomponensiPontszam = kurKomponensiPontszam;
    }

    public String getKurHibaPontszam() {
        return kurHibaPontszam;
    }

    public void setKurHibaPontszam(String kurHibaPontszam) {
        this.kurHibaPontszam = kurHibaPontszam;
    }
    
    
    
    
    
}
