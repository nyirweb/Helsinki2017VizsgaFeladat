package helsinki2017.model;

public class RovidProgramModel 
{
    String nev;
    String orszagKod;
    String technikaiPontszam;
    String komponensPontszam;
    String hibapont;

    public RovidProgramModel() {
    }

    public String getNev() {
        return nev;
    }

    public void setNev(String nev) {
        this.nev = nev;
    }

    public String getOrszagKod() {
        return orszagKod;
    }

    public void setOrszagKod(String orszagKod) {
        this.orszagKod = orszagKod;
    }

    public String getTechnikaiPontszam() {
        return technikaiPontszam;
    }

    public void setTechnikaiPontszam(String technikaiPontszam) {
        this.technikaiPontszam = technikaiPontszam;
    }

    public String getKomponensPontszam() {
        return komponensPontszam;
    }

    public void setKomponensPontszam(String komponensPontszam) {
        this.komponensPontszam = komponensPontszam;
    }

    public String getHibapont() {
        return hibapont;
    }

    public void setHibapont(String hibapont) {
        this.hibapont = hibapont;
    }
    
    
    
    
}
