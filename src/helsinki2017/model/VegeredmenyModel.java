package helsinki2017.model;

public class VegeredmenyModel 
{
    String nev;
    String orszagKod;
    String osszPontszam;

    public VegeredmenyModel() {
    }
    
    public String getNev() {
        return nev;
    }

    public void setNev(String nev) {
        this.nev = nev;
    }

    public String getOrszagKod() {
        return orszagKod;
    }

    public void setOrszagKod(String orszagKod) {
        this.orszagKod = orszagKod;
    }

    public String getOsszPontszam() {
        return osszPontszam;
    }

    public void setOsszPontszam(String osszPontszam) {
        this.osszPontszam = osszPontszam;
    }
    
    
}
